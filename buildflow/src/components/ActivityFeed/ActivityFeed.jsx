function ActivityFeed({ activities }) {
  const getIcon = (type) => {
    return activityIcons[type] || activityIcons.update;
  };

  const getIconClass = (type) => {
    const classes = {
      update: 'activity-icon-update',
      order: 'activity-icon-order',
      invoice: 'activity-icon-invoice',
      risk: 'activity-icon-risk',
      team: 'activity-icon-team',
      milestone: 'activity-icon-milestone',
    };
    return classes[type] || 'activity-icon-update';
  };

  return (
    <div className="activity-feed">
      {activities.map((activity) => (
        <div key={activity.id} className="activity-item">
          <div className={`activity-icon ${getIconClass(activity.type)}`}>
            {getIcon(activity.type)}
          </div>
          <div className="activity-content">
            <span className="activity-message">{activity.message}</span>
            <span className="activity-time">{activity.time}</span>
          </div>
        </div>
      ))}
    </div>
  );
}

export default ActivityFeed;
